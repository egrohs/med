import axios from 'axios';

import { IPRESCRIPTION } from '@/shared/model/prescription.model';

const baseApiUrl = 'api/prescriptions';

export default class PRESCRIPTIONService {
  public find(id: number): Promise<IPRESCRIPTION> {
    return new Promise<IPRESCRIPTION>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IPRESCRIPTION): Promise<IPRESCRIPTION> {
    return new Promise<IPRESCRIPTION>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IPRESCRIPTION): Promise<IPRESCRIPTION> {
    return new Promise<IPRESCRIPTION>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
